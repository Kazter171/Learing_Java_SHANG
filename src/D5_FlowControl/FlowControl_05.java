package D5_FlowControl;

public class FlowControl_05 {
    public static void main(String[] args){
        //TODO: 分支执行

        //TODO: 多分支 switch (等值判断)
        //如果需要强制进入switch选择，加default
        //break跳出分支结构
        int i = 10;
        switch (i){
            case 10 :
                System.out.println("01");
                break;
            case 20 :
                System.out.println("02");
            case 30 :
                System.out.println("03");
            default:
                System.out.println("04");
        }
        }
    }
